export const formatPrice = (property) => {
  if (property.type === "rent") {
    return "£" + property.pricePerMonth.toLocaleString() + " pcm";
  }
  if (property.price >= 1000000) {
    return "£" + (property.price / 1000000).toFixed(2).replace(".00", "") + "m";
  }
  return "£" + property.price.toLocaleString();
};